
public class Metodos {
    public int suma(int v1, int v2){//Creamos metodo suma
        int result = v1 + v2;
        return result;
    }
    public int resta(int v1, int v2){//Creamos metodo resta
        int result = v1 - v2;
        return result;
    }
    public int multiplicacion(int v1, int v2){//Creamos metodo multipliacion
        int result = v1 * v2;
        return result;
    }
    public int division(int v1, int v2){//Creamos metodo division
        int result = v1 / v2;
        return result;
    }   
}
